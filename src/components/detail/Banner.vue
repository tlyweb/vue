<template>
	<div>
		<div class="banner" @click="handleBannerClick">
			<img class="banner-img" :src="bannerImg" alt="">
			<div class="banner-info">
				<div class="banner-title">{{sightName}}</div>
				<div class="banner-number">
					<span class="icon iconfont icontupian banner-icon"></span>
					{{this.bannerImgs.length}}
				</div>
			</div>
		</div>
		<FadeAnimation>
			<Gallary
					:imgs="bannerImgs"
					v-show="showGallary"
					@close="handleGallaryClose"
			></Gallary>
		</FadeAnimation>
	</div>

</template>

<script>
	import Gallary from "../gallary/Gallary";
	import FadeAnimation from "../fade/FadeAnimation";
	export default {
		name: "Banner",
		props:{
			sightName:String,
			bannerImg:String,
			bannerImgs:Array,
		},
		data(){
			return{
				showGallary:false,
			}
		},
		methods:{
			//点击图片放大
			handleBannerClick(){
				this.showGallary = true;
			},
			//点击关闭事件
			handleGallaryClose(){
				this.showGallary = false;
			}
		},
		components:{
			Gallary,
			FadeAnimation
		}
	}
</script>

